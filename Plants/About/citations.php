Citation and Acknowledgements

Please cite the PLANTS Database as:

USDA, NRCS. 2016. The PLANTS Database (http://plants.usda.gov, 23 October 2016). National Plant Data Team, Greensboro, NC 27401-4901 USA.

Taxonomic Checklist Data Contributors:
John Kartesz, Biota of North America Program, provided the foundational U.S. vascular plant checklist, state and county distribution, and the original duration, growth habit, and native status data used in PLANTS.  NRCS is pleased to have significantly funded BONAP and supported the development of this critical land management information from 1990 until 2006.  Luc Brouillet (Université de Montréal) and Canadian colleagues generously donated Canadian vascular plant names with distribution and attributes in 2006. Vascular data—especially native status and scientific name authorities and orthography—have since been partially revised by Mark Skinner, former NPDT employee, and will continue to be revised internally and by external specialists for the foreseeable future.

Robert Egan (U. Nebraska at Omaha) and Theodore Esslinger (North Dakota State University) provided the North American lichen checklist, which is periodically published in The Bryologist.

Lewis Anderson and Molly McMullen of Duke University provided the original North American moss checklist, and it was updated by Marshall Crosby of the Missouri Botanical Garden.

Ray Stotler and Barbara Crandall-Stotler of Southern Illinois University, Carbondale provided the North American liverwort and hornwort checklists.

Checklist higher taxonomy was developed by Mark Skinner and Deiter Wilken.

